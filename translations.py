# ---------------------------------------------------------------------------
# Runtime state & accessor
# ---------------------------------------------------------------------------

_current_lang = DEFAULT_LANG


def set_language(code: str) -> None:
    global _current_lang
    if code in LANG_CODES:
        _current_lang = code


def get_language() -> str:
    return _current_lang


def get_lang_name(code: str = None) -> str:
    """Return the display name for a language code."""
    if code is None:
        code = _current_lang
    for name, c in LANGUAGES.items():
        if c == code:
            return name
    return code


def t(key: str, **kwargs) -> str:
    """Get translated string for the current language.
    Supports {placeholder} formatting via kwargs.
    Falls back to English, then to the key itself.
    """
    entry = STRINGS.get(key)
    if entry is None:
        return key
    text = entry.get(_current_lang) or entry.get("en") or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text


def t_theme(internal_name: str) -> str:
    """Translate a theme internal name (THEMES dict key) to the current language."""
    return t(f"theme.{internal_name}")
